
#include "LanguageManager.h"

LanguageManager& LanguageManager::Instance() {
    static LanguageManager instance;
    return instance;
}

LanguageManager::LanguageManager() : currentLang(Lang::EN) {
    Load();
}

void LanguageManager::SetLanguage(Lang lang) {
    currentLang = lang;
}

LanguageManager::Lang LanguageManager::GetLanguage() const {
    return currentLang;
}

const std::string& LanguageManager::Tr(const std::string& key) const {
    if (currentLang == Lang::ZH_CN) {
        auto it = dictZH.find(key);
        if (it != dictZH.end()) return it->second;
    } else {
        auto it = dictEN.find(key);
        if (it != dictEN.end()) return it->second;
    }
    static std::string empty;
    return empty;
}

